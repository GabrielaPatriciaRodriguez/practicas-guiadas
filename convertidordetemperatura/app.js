// Vamos a preguntar la temperatura en Celsius a traves de una ventana emergente (Prompt)
var temperature = parseInt(prompt ("Cual es la temperatura en celsius?"));
// Convertir Celsius a Farenheit
var convertToFarenheit = (temperature*1.8) + 32;
// Mostrar en la consola a traves de un console.log
console.log("La temperatura en farenheit es " + convertToFarenheit);
document.write("La temperatura en farenheit es " + convertToFarenheit);

